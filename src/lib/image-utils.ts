import { SUPPORTED_IMAGE_TYPES, MAX_IMAGE_SIZE, MAX_UPLOAD_SIZE } from './constants';

export function validateImageType(file: File): boolean {
  return SUPPORTED_IMAGE_TYPES.includes(file.type);
}

export function validateImageSize(file: File): { valid: boolean; needsCompression: boolean } {
  if (file.size > MAX_UPLOAD_SIZE) {
    return { valid: false, needsCompression: false };
  }
  if (file.size > MAX_IMAGE_SIZE) {
    return { valid: true, needsCompression: true };
  }
  return { valid: true, needsCompression: false };
}

export async function compressImage(file: File): Promise<Blob> {
  return new Promise((resolve, reject) => {
    const img = new Image();
    const canvas = document.createElement('canvas');
    const ctx = canvas.getContext('2d');

    img.onload = () => {
      // Calculate new dimensions (max 1920px)
      let { width, height } = img;
      const maxDimension = 1920;

      if (width > maxDimension || height > maxDimension) {
        if (width > height) {
          height = (height / width) * maxDimension;
          width = maxDimension;
        } else {
          width = (width / height) * maxDimension;
          height = maxDimension;
        }
      }

      canvas.width = width;
      canvas.height = height;
      ctx?.drawImage(img, 0, 0, width, height);

      canvas.toBlob(
        (blob) => {
          if (blob) {
            resolve(blob);
          } else {
            reject(new Error('Failed to compress image'));
          }
        },
        'image/jpeg',
        0.8
      );
    };

    img.onerror = () => reject(new Error('Failed to load image'));
    img.src = URL.createObjectURL(file);
  });
}

export async function fileToBase64(file: File | Blob): Promise<string> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => {
      const result = reader.result as string;
      // Remove data URL prefix
      const base64 = result.split(',')[1];
      resolve(base64);
    };
    reader.onerror = reject;
    reader.readAsDataURL(file);
  });
}
